#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t			len;
	size_t			i;
	char			*result;

	len = ft_strlen(s);
	result = malloc(sizeof(char *) * len + 1);
	if (!result)
		return (NULL);
	i = 0;
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
